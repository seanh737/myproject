package oo.abstraction;

public class Coffeebeans {
	String name;
	String origin;
	String Flavor;
	int altitude;
	String roast; 
	
	public Coffeebeans(String name, String origin, String Flavor, int altitude, String roast){
		this.name = name;
		this.origin = origin;
		this.Flavor = Flavor;
		this.altitude = altitude;
		this.roast = roast;
	}
}
